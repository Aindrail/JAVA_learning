package loops.do_while;

public class example {
 public static void main(String[] args) {
     int n = 0 ; 
     do { 
         System.out.println();
         System.out.println();
         System.out.println();
         System.out.println(" The number is "+n);
         System.out.println();
         System.out.println();
         System.out.println();
     } while ( n!=0);
 }   
}
