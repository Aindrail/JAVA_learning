package operators;

public class comparision {
    public static void main( String[] args){
        int a = 3;
        int b = 5;
        System.out.println(a>b);
        System.out.println(b>a);
        System.out.println(b>=a);
        System.out.println(b<=a);
        System.out.println(b==a);
        System.out.println(b!=a);
    }
    
}
